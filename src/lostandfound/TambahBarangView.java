    private void tambahBarangButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahBarangButtonActionPerformed
        
        String namaBarang = namaBarangTextField.getText();
        String jenisBarang = jenisBarangComboBox.getSelectedItem().toString();
        int tgl = datePicker.getModel().getDay();
        int bln = datePicker.getModel().getMonth()+1;
        int thn = datePicker.getModel().getYear();
        String tglDitemukan=""+thn+"-"+bln+"-"+tgl;
        String ket = keteranganTextArea.getText();
        String namaPenemu = namaPenemuTextField.getText();
        String noKtp = noKtpTextField.getText();
        String noTelepon = noTeleponTextField.getText();
        
        //validate input
        if(namaBarang.isEmpty()||jenisBarang.isEmpty()||tglDitemukan.isEmpty()||namaPenemu.isEmpty()){
            JOptionPane.showMessageDialog(null, "Harap isi semua kolom yang wajib diisi!", "Pesan", WARNING_MESSAGE);
            return;
        }
        
        if(!noKtp.equals("")){
            if(!noKtp.matches(".*\\d.*") || noKtp.length()!=16){
            JOptionPane.showMessageDialog(null, "Harap cek input no KTP Anda!", "Pesan", WARNING_MESSAGE);
            return;
            }
        }
        
        if(!noTelepon.equals("")){
            if(!noTelepon.matches(".*\\d.*")){
            JOptionPane.showMessageDialog(null, "Harap cek input no telepon Anda!", "Pesan", WARNING_MESSAGE);
            return;
            }
        }
        
        boolean success = barang.tambahBarang(namaBarang, jenisBarang, tglDitemukan, ket, namaPenemu, noKtp, noTelepon);
        
        if(success){
            JOptionPane.showMessageDialog(null, "Data berhasil diinput!", "Pesan", INFORMATION_MESSAGE);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Maaf, data gagal diinput! Cek kembali input Anda!", "Pesan", WARNING_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_tambahBarangButtonActionPerformed

